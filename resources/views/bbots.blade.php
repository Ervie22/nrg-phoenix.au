				<h3>Bots for Vaccine Delivery</h3>

				<p>Spreading the word about vaccination is a bigger task before immunising people against COVID-19. During this situation, the usage of bot enables ease of availability of vaccination appointments. This shows that usage of bots and AI can benefit the healthcare industry.</p>

